<h1>A11yTalks Mission and Values</h1>
<h2>Mission</h2>

<p>Normalizing digital accessibility through education.</p>

<h2>Values</h2>

<h3>Accessibility</h3>

<p>In addition to curating content focussed on digital accessibility, we aim to make our digital presence, events, and activities as accessible as we possibly can.</p>

<h3>Education</h3>

<p>We come together to curate and share knowledge and experiences that help people grow professionally and personally. Our content evolves understanding, builds empathy, and provides language and tools to improve digital accessibility in our jobs and lives.</p>

<h3>Diversity and Inclusion</h3>

<p>We make a conscious effort to give <abbr title="People with Disabilities">PwD</abbr>, <abbr title="Black, Indigenous, People of Color">BIPOC</abbr>, <abbr title="Lesbian, Gay, Bisexual, Trans, Queer, Intersex, Asexual and more">LGBTQIA+</abbr>, women, and other marginalized groups in tech a place to share their knowledge and skills through their voices and lived experiences.
Representation of marginalized groups in positions of influence foster belonging and normalization of the human experience. It also encourages other marginalized people to share, building a better community.</p>

<h3>Community</h3>

<p>Learning, sharing, and growing together allow us to impact our mission more than anyone can do on their own. We are all in this together, and together everyone accomplishes more.</p>

<h3>Authenticity</h3>

<p>Sharing credible and genuine knowledge and experiences enable us to be a trusted resource within the digital accessibility community.</p>
